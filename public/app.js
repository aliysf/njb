const state = {
  lang: localStorage.getItem("pt-njb-language") || "id",
  filter: "all"
};

const $ = (selector, scope = document) => scope.querySelector(selector);
const $$ = (selector, scope = document) => [...scope.querySelectorAll(selector)];

function setLanguage(lang) {
  state.lang = lang;
  localStorage.setItem("pt-njb-language", lang);
  document.documentElement.lang = lang;
  document.title = lang === "id" ? "PT NJB | Profil Perusahaan" : "PT NJB | Company Profile";

  $$("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  $$("[data-lang]").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });

  renderDynamicSections();
}

function renderDynamicSections() {
  const content = translations[state.lang];

  $("[data-service-grid]").innerHTML = content.services
    .map(
      (service) => `
        <article class="service-card reveal">
          <span class="service-icon">${service.icon}</span>
          <h3>${service.title}</h3>
          <p>${service.body}</p>
          <ul>${service.points.map((point) => `<li>${point}</li>`).join("")}</ul>
        </article>
      `
    )
    .join("");

  $("[data-timeline]").innerHTML = content.timeline
    .map(
      (item) => `
        <article class="timeline-item reveal">
          <h3>${item.title}</h3>
          <p>${item.body}</p>
        </article>
      `
    )
    .join("");

  $("[data-filter-bar]").innerHTML = Object.entries(content.filters)
    .map(
      ([key, label]) => `
        <button type="button" role="tab" class="${state.filter === key ? "active" : ""}" data-filter="${key}">
          ${label}
        </button>
      `
    )
    .join("");

  const filteredPortfolio =
    state.filter === "all" ? content.portfolio : content.portfolio.filter((item) => item.category === state.filter);

  $("[data-portfolio-grid]").innerHTML = filteredPortfolio
    .map(
      (item) => `
        <article class="portfolio-card reveal" style="--card-bg: ${item.color}">
          <span class="tag">${content.filters[item.category]}</span>
          <h3>${item.title}</h3>
          <p>${item.body}</p>
        </article>
      `
    )
    .join("");

  $("[data-values-grid]").innerHTML = content.values
    .map(
      (value) => `
        <article class="value-card reveal">
          <strong>${value.mark}</strong>
          <h3>${value.title}</h3>
          <p>${value.body}</p>
        </article>
      `
    )
    .join("");

  observeReveals();
}

function observeReveals() {
  const revealElements = $$(".reveal:not(.is-visible)");

  if (!("IntersectionObserver" in window)) {
    revealElements.forEach((element) => element.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.14 }
  );

  revealElements.forEach((element) => observer.observe(element));
}

function animateCounters() {
  const counters = $$("[data-count-to]");
  let started = false;

  const run = () => {
    if (started) {
      return;
    }

    started = true;
    counters.forEach((counter) => {
      const target = Number(counter.dataset.countTo);
      const suffix = target === 100 ? "%" : "+";
      const duration = 1000;
      const start = performance.now();

      const tick = (now) => {
        const progress = Math.min((now - start) / duration, 1);
        const value = Math.round(target * progress);
        counter.textContent = `${value}${suffix}`;

        if (progress < 1) {
          requestAnimationFrame(tick);
        }
      };

      requestAnimationFrame(tick);
    });
  };

  const card = $(".profile-card");
  if (!card || !("IntersectionObserver" in window)) {
    run();
    return;
  }

  const observer = new IntersectionObserver(
    ([entry]) => {
      if (entry.isIntersecting) {
        run();
        observer.disconnect();
      }
    },
    { threshold: 0.35 }
  );

  observer.observe(card);
}

function bindEvents() {
  const nav = $("[data-nav]");
  const navToggle = $("[data-nav-toggle]");

  navToggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("open");
    navToggle.setAttribute("aria-expanded", String(isOpen));
  });

  $$("[data-nav] a").forEach((link) => {
    link.addEventListener("click", () => {
      nav.classList.remove("open");
      navToggle.setAttribute("aria-expanded", "false");
    });
  });

  $$("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.lang));
  });

  document.addEventListener("click", (event) => {
    const filterButton = event.target.closest("[data-filter]");
    if (filterButton) {
      state.filter = filterButton.dataset.filter;
      renderDynamicSections();
    }
  });

  window.addEventListener("scroll", () => {
    $("[data-header]").classList.toggle("is-scrolled", window.scrollY > 12);
  });

  $("[data-contact-form]").addEventListener("submit", (event) => {
    event.preventDefault();
    const form = event.currentTarget;
    const formData = new FormData(form);
    const name = String(formData.get("name") || "").trim();
    const email = String(formData.get("email") || "").trim();
    const message = String(formData.get("message") || "").trim();
    const note = $("[data-form-note]");
    const content = translations[state.lang];

    if (!name || !email || !message) {
      note.textContent = content.formError;
      return;
    }

    const subject = encodeURIComponent(`PT NJB inquiry from ${name}`);
    const body = encodeURIComponent(`Name: ${name}\nEmail: ${email}\n\n${message}`);
    note.textContent = content.formReady;
    window.location.href = `mailto:info@pt-njb.co.id?subject=${subject}&body=${body}`;
  });
}

document.addEventListener("DOMContentLoaded", () => {
  $("[data-year]").textContent = new Date().getFullYear();
  bindEvents();
  setLanguage(state.lang);
  animateCounters();
});
